import org.conservationmeasures.eam.main.MainWindow;

public class ActionExportTable extends ViewAction
{
	public ActionExportTable(MainWindow mainWindowToUse)
	{
		super(mainWindowToUse, getLabel());
	}

	private static String getLabel()
	{
		return EAM.text("Action|Table Delimited Data");
	}

	public String getToolTipText()
	{
		return EAM.text("TT|Export this view's table to a tab delimited file");
	}
}
